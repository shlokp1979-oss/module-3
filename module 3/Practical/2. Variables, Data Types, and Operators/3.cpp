// 3. Operator Demonstration
// o Write a C++ program that demonstrates arithmetic, relational, logical, and bitwise
// operators. Perform operations using each type of operator and display the results.
// o Objective: Reinforce understanding of different types of operatorsin C++
#include <iostream>
using namespace std;
int main() {
    int a = 5, b = 3;
    cout << a + b << endl;
    cout << a - b << endl;
    cout << a * b << endl;
    cout << a / b << endl;
    cout << (a > b) << endl;
    cout << (a == b) << endl;
    cout << (a && b) << endl;
    cout << (a || b) << endl;
    cout << (a & b) << endl;
    cout << (a | b) << endl;
    cout << (a ^ b) << endl;
    cout << (~a) << endl;
    return 0;
}
