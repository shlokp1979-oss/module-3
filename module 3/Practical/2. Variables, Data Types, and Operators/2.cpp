// 2. Type Conversion
// o Write a C++ program that performs both implicit and explicit type conversions and
// prints the results.
// TOPS Technologies 2024
// o Objective: Practice type casting in C++
#include <iostream>
using namespace std;
int main() {
    int a = 10;
    double b = a;
    double x = 9.8;
    int y = (int)x;
    cout << b << endl;
    cout << y << endl;
    return 0;
}
